SELECT
  ROW_NUMBER() OVER (ORDER BY INVMA.MA002) AS INVMA_ID,
  MA001,--分類方式(4:生管)
  MA002,--產品代號
  MA003 --產品機種別
FROM INVMA
WHERE MA001 = '4'

--機種別與產品之關連
SELECT  *
FROM (SELECT
  MA.MA003,
  MB.MB008,
  MB.MB001
FROM INVMB AS MB
LEFT JOIN INVMA AS MA
  ON MB.MB008 = MA.MA002
WHERE MA.MA003 IS NOT NULL) AS INVMAB
ORDER BY MA003


